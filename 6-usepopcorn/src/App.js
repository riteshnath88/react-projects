const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

const KEY = "966a7b02";
const tempQuery = "Mission Impossible";

export default function App() {
  const [movies, setMovies] = useState([]);
  // We can have a function as the inital value and it will be executed only once during the first upload. Since the data is store in the
  // localStorage as string, we need to do JSON.parse() to convert it to object.
  const [watched, setWatched] = useState(function () {
    const storedValue = localStorage.getItem("watched");
    return JSON.parse(storedValue);
  });
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  function handleSelectedMovie(id) {
    setSelectedId((selectedId) => (selectedId === id ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }

  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
    // We can store the watched list in localStorage either in this function as below or we can use useEffect.
    // localStorage.setItem("watched", JSON.stringify([...watched, movie]));
  }

  function handleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imdbID !== id));
  }

  // Inside the function, watched is still not updated. So we use spread operator followed by the movie but in case of useEffect, the function is
  // called only after the dependency list is updated. So we can directly use watched.
  useEffect(
    function () {
      localStorage.setItem("watched", JSON.stringify(watched));
    },
    [watched]
  );

  // To listen to global key press event. Pressing of 'Escape' key.
  useEffect(
    function () {
      // Following abort controller is used to prevent race condition i.e. remove previous API calls when a new call is made through the same API call function
      // AbortController is a browser API and not related to React just like the getch function.
      // To connect the abort controller to the fetch function, we have to specify a second parameter {signal: controller.signal}
      // Then finally in the clean up function we sepcify the follosing
      // return function () {
      //   controller.abort();
      // };

      const controller = new AbortController();

      async function fetchMovies() {
        try {
          setIsLoading(true);
          setError("");
          const res = await fetch(
            `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`,
            { signal: controller.signal }
          );

          // If network connection is broken, we won't get the ok status in the response and for that we need to throw a new error
          if (!res.ok) {
            throw new Error(
              "Something went wrong while fetching the movies..."
            );
          }

          const data = await res.json();

          if (data.Response === "False") {
            throw new Error("Movie not found...");
          }

          setMovies(data.Search);
          setError(""); // To nullify the effect of AbortError controller.
        } catch (err) {
          console.error(err.message);
          if (err.name !== "AbortError") {
            // So that the abort operations performed by Abort Controller is not considered as an error
            setError(err.message);
          }
        } finally {
          setIsLoading(false);
        }
      }

      if (query.length < 3) {
        setMovies([]);
        setError("");
        return;
      }

      handleCloseMovie();
      fetchMovies();

      return function () {
        controller.abort();
      };
    },
    [query]
  ); // In the json object returned by the API, movie data is stored inside Search key. So we write data.Search.

  return (
    <>
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {/* If the API data is loading and there is no error yet, we are displaying the <Loader/>
          If the isLoading is already false i.e. !isLoading is true and there is no error, then load the movies
          If the error is true then load the error message. */}
          {/* {isLoading ? <Loading /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectedMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
      </Main>
    </>
  );
}

