function Search({ query, setQuery }) {
  // We want that whenever the page loads, the focuss or cursor should be on the search box.
  // useEffect(function () {
  //   const el = document.querySelector(".search");
  //   el.focus();
  // }, []);

  // Achieving the above functionality using refs
  const inputEl = useRef(null);

  useEffect(
    function () {
      // Whenever we press the enter key, we want to re-focus on the search box and also remove the current selections
      function callback(e) {
        // If the current focuss is on the search box i.e. inputEl, we don't want to delete the text on accidentally clicking Enter
        if (document.activeElement === inputEl.current) {
          return;
        }

        if (e.code === "Enter") {
          // element.current will give the current DOM element on which we can do focuss
          inputEl.current.focus();
          setQuery("");
        }
      }
      document.addEventListener("keydown", callback);
      // For cleanup
      return () => document.addEventListener("keydown", callback);
    },
    [setQuery]
  );

  return (
    <input
      className="search"
      type="text"
      placeholder="Search movies..."
      value={query}
      onChange={(e) => setQuery(e.target.value)}
      ref={inputEl}
    />
  );
}

